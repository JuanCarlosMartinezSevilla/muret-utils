def add_numbers(num1, num2):
    """
    Add two numbers together.

    Parameters:
    num1 (int or float): The first number.
    num2 (int or float): The second number.

    Returns:
    int or float: The sum of num1 and num2.

    Example:
    >>> add_numbers(5, 3)
    8
    >>> add_numbers(2.5, 4.5)
    7.0
    """
    return num1 + num2
